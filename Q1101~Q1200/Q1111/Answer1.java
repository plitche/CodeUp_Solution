	// 어떤 정수가 입력되면 %를 붙여 출력하시오.
	
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int number = sc.nextInt();
		
		System.out.println(number + "%");
		
		sc.close();
	}

